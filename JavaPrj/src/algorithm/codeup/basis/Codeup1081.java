package algorithm.codeup.basis;
import java.util.*;
public class Codeup1081 {
	public static void main(String[] args) {
		
		Scanner scn = new Scanner(System.in);
		int a = scn.nextInt();
		int b = scn.nextInt();
		for(int i=0; i<a; i++) {
			for(int j=0; j<b; j++) {
				System.out.println((i+1)+" "+(j+1));
			}
		}
		
		
	}

}

